package slideDeckExercises;

/**
 * 
 */
public class Mountain {

	private String name;
	private int height;
	
	
	/**
	 * Set method which sets the name of the mountain
	 * @param name - the name to be set
	 */
	public void setName(String name) {
		this.name = name;
	}
	
	/**
	 * Get method which returns the name of the mountain
	 * @return the name of the mountain
	 */
	public String getName() {
		return name;
	}

	/**
	 * Get method which returns the height of the mountain
	 * @return the height of the mountain
	 */
	public int getHeight() {
		return height;
	}

	/**
	 * Set method which sets the height of the mountain
	 * @param height - the height to set
	 */
	public void setHeight(int height) {
		this.height = height;
	}

	
}
